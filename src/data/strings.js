// Helper: t('key', language) → returns the localized string
export function t(key, language) {
  const value = STRINGS[language]?.[key] ?? STRINGS.en[key]
  if (value === undefined) {
    console.warn(`Missing translation key: ${key}`)
    return key
  }
  return value
}
